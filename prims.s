apply:
    push rbp
    mov r13, rbp
    mov rbp, rsp

    mov rsi, PVAR(0)        ; gets proc
    mov rdi, PARAM_COUNT    ; gets n_params
    dec rdi                 ; rdi <- n_params -1
    mov r8, PVAR(rdi)       ; r8 <- arg(n_params - 1)
    mov r9, r8              ; r9 <- list argument of apply
    mov r10, rdi              ; index r10 = i = 1
    dec r10

.loop:
    cmp r10, 0            ; i < n_params - 1
    jle .next               ;
    mov r11, PVAR(r10)      ; r11 <- arg(i)
    mov r12, r9             ; dup tail
    MAKE_PAIR(r9, r11, r12) ; p <- Pair(PVAR(i), p)
    dec r10                 ; i++
    jmp .loop

.next:
    mov r10, 0              ; i = 0
    push 43962    ; same as applic

    REV_LIST rdi, r9
    mov r9, rdi

.loop2:
    cmp r9, SOB_NIL_ADDRESS ; if r9 == Nil
    je .next2               ; done
    inc r10                ; i++
    CAR r11, r9             ; r11 <- (car arglist)
    push r11                ; push arg
    CDR r11, r9
    mov r9, r11             ; arglist <- (cdr arglist)
    jmp .loop2

.next2:
    push r10                ; push n_params

    cmp byte [rsi], T_CLOSURE ;if not closure
    jnz .error                 ;throw error
    mov rdi, qword [rsi + TYPE_SIZE] ;rdi <- lex_env
    push rdi 
    mov rdi, qword [rbp + 8 * 1]
    push rdi   
    mov rdi, qword [rsi + TYPE_SIZE + WORD_SIZE] ;rdi <- lambda_body
    mov r9, r10
    add r9, 5
    SHIFT_FRAME r9
.aftershift:
    mov rbp, r13
    
    jmp rdi
    
.error:
      mov rdi, .errstring
      call printf
      jmp .end_applic
.errstring:
      db "lambdaSimpleErr", 0
.end_applic:
leave
ret

